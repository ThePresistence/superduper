      SUBROUTINE INIPOT (DELTA,OMEGA,LMZZ,IOP,MMPOT)
C     *
C     DEFINE PARAMETERS FOR SEMIEMPIRICAL ELECTROSTATIC POTENTIALS.
C     THESE PARAMETERS ARE AVAILABLE ONLY FOR MNDO AND AM1.
C     (1) D. BAKOWIES AND W. THIEL, J.COMP.CHEM. 17, 87 (1996).
C     (2) G.P. FORD AND B. WANG, J.COMP.CHEM. 14, 1101 (1993).
C     *
      USE LIMIT, ONLY: LM1, LMZ
      IMPLICIT DOUBLE PRECISION (A-H,O-Z)
      COMMON
     ./ATOMS / NUMAT,NAT(LM1),NFIRST(LM1),NLAST(LM1)
     ./CONSTN/ ZERO,ONE,TWO,THREE,FOUR,PT5,PT25
     ./MULTIP/ DD(6,0:LMZ),PO(9,0:LMZ)
     ./NBFILE/ NBF(20)
     ./PAROPT/ USS(LMZ*6),ALP(LMZ)
      DIMENSION DELTA(LMZZ),OMEGA(LMZZ)
C *** FILE NUMBERS.
      NB6    = NBF(6)
C *** INITIALIZATION.
      IF(IOP.GT.0) RETURN
      IF(IOP.EQ.-5 .OR. IOP.EQ.-6 .OR. IOP.EQ.-8 .OR. IOP.EQ.-9
     1             .OR. IOP.EQ.-22.OR. IOP.EQ.-23) RETURN
      IF(MMPOT.LT.3) RETURN
      DO 10 I=1,NUMAT
      NI        = NAT(I)
      DELTA(NI) = ZERO
      OMEGA(NI) = ALP(NI)
   10 CONTINUE
C *** ONE-PARAMETER POTENTIAL FOR AM1 (REF.1).
      IF(MMPOT.EQ.4 .AND. IOP.EQ.-2) THEN
         OMEGA(1) = 3.56510D0
         OMEGA(6) = 2.71903D0
         OMEGA(7) = 3.25328D0
         OMEGA(8) = 3.61827D0
         DO 20 I=1,NUMAT
         NI = NAT(I)
         IF(NI.NE.1 .AND. NI.NE.6 .AND. NI.NE.7 .AND. NI.NE.8) THEN
            WRITE(NB6,510)
            WRITE(NB6,520) NI
         ENDIF
   20    CONTINUE
      ENDIF
C *** ONE-PARAMETER POTENTIAL FOR MNDO (REF.1).
      IF(MMPOT.EQ.4 .AND. IOP.EQ.0) THEN
         OMEGA(1) = 3.44301D0
         OMEGA(3) = 1.11988D0
         OMEGA(6) = 2.89588D0
         OMEGA(7) = 3.34883D0
         OMEGA(8) = 3.67920D0
         DO 30 I=1,NUMAT
         NI = NAT(I)
         IF(NI.NE.1 .AND. NI.NE.3 .AND. (NI.LT.6 .OR. NI.GT.8)) THEN
            WRITE(NB6,530)
            WRITE(NB6,520) NI
         ENDIF
   30    CONTINUE
      ENDIF
C *** TWO-PARAMETER POTENTIAL FOR AM1 (REF.1).
      IF(MMPOT.EQ.5 .AND. IOP.EQ.-2) THEN
         DELTA(1) = 0.10080D0
         DELTA(6) = 0.12996D0
         DELTA(7) = 0.32275D0
         DELTA(8) = 0.26524D0
         OMEGA(1) = 3.38541D0
         OMEGA(6) = 2.98988D0
         OMEGA(7) = 4.72260D0
         OMEGA(8) = 4.95208D0
         DO 40 I=1,NUMAT
         NI = NAT(I)
         IF(NI.NE.1 .AND. NI.NE.6 .AND. NI.NE.7 .AND. NI.NE.8) THEN
            WRITE(NB6,540)
            WRITE(NB6,520) NI
         ENDIF
   40    CONTINUE
      ENDIF
C *** TWO-PARAMETER POTENTIAL FOR MNDO (REF.1).
      IF(MMPOT.EQ.5 .AND. IOP.EQ.0) THEN
         DELTA(1) = 0.14036D0
         DELTA(6) = 0.22942D0
         DELTA(7) = 0.42741D0
         DELTA(8) = 0.31758D0
         OMEGA(1) = 3.48166D0
         OMEGA(6) = 3.54244D0
         OMEGA(7) = 5.84706D0
         OMEGA(8) = 5.51133D0
         DO 50 I=1,NUMAT
         NI = NAT(I)
         IF(NI.NE.1 .AND. NI.NE.6 .AND. NI.NE.7 .AND. NI.NE.8) THEN
            WRITE(NB6,550)
            WRITE(NB6,520) NI
         ENDIF
   50    CONTINUE
      ENDIF
C *** TWO-PARAMETER POTENTIAL FOR AM1 (REF.2).
      IF(MMPOT.EQ.6 .AND. IOP.EQ.-2) THEN
         DELTA(1) = 0.05D0
         DELTA(6) = 0.63D0
         DELTA(7) = 0.64D0
         DELTA(8) = 0.67D0
         DELTA(9) = 0.29D0
         DELTA(17)= 0.31D0
         OMEGA(1) = 2.92D0
         OMEGA(6) = 4.36D0
         OMEGA(7) = 6.95D0
         OMEGA(8) = 9.22D0
         OMEGA(9) = 5.53D0
         OMEGA(17)= 3.32D0
         DO 60 I=1,NUMAT
         NI = NAT(I)
         IF(NI.NE.1 .AND. NI.NE.6 .AND. NI.NE.7 .AND. NI.NE.8
     1              .AND. NI.NE.9 .AND. NI.NE.17) THEN
            WRITE(NB6,560)
            WRITE(NB6,520) NI
         ENDIF
   60    CONTINUE
      ENDIF
C *** CHARGE SEPARATIONS AND ADDITIVE TERMS FOR TEST CHARGE.
C     MMPOT=3-7: TEST CHARGE TREATED AS CLASSICAL MONOPOLE.
C     MMPOT=8  : TEST CHARGE TREATED AS HYDROGEN ATOM.
      DO 70 I=1,6
      DD(I,0) = ZERO
   70 CONTINUE
      DO 80 I=1,9
      PO(I,0) = ZERO
   80 CONTINUE
      IF(MMPOT.EQ.8) THEN
         PO(1,0) = PO(1,1)
         PO(9,0) = PO(1,1)
      ENDIF
      RETURN
  510 FORMAT(/  1X,'PARAMETERS FOR THE AM1 ELECTROSTATIC POTENTIAL',
     1          1X,'(MMPOT=4) ARE AVAILABLE ONLY FOR H,C,N,O.')
  520 FORMAT(   1X,'STANDARD PARAMETERS (MMPOT=3) ARE USED FOR',
     1          1X,'ELEMENT NI =',I3)
  530 FORMAT(/  1X,'PARAMETERS FOR THE MNDO ELECTROSTATIC POTENTIAL',
     1          1X,'(MMPOT=4) ARE AVAILABLE ONLY FOR H,Li,C,N,O.')
  540 FORMAT(/  1X,'PARAMETERS FOR THE AM1 ELECTROSTATIC POTENTIAL',
     1          1X,'(MMPOT=5) ARE AVAILABLE ONLY FOR H,C,N,O.')
  550 FORMAT(/  1X,'PARAMETERS FOR THE MNDO ELECTROSTATIC POTENTIAL',
     1          1X,'(MMPOT=5) ARE AVAILABLE ONLY FOR H,C,N,O.')
  560 FORMAT(/  1X,'PARAMETERS FOR THE AM1 ELECTROSTATIC POTENTIAL',
     1          1X,'(MMPOT=6) ARE AVAILABLE ONLY FOR H,C,N,O,F,Cl.')
      END
