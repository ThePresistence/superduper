      SUBROUTINE INPSCF (JPRINT)
C     *
C     INPUT OF SPECIFIC OPTIONS FOR INTEGRALS AND SCF TREATMENT.
C     *
      IMPLICIT DOUBLE PRECISION (A-H,O-Z)
      COMMON
     ./CONSTF/ A0,AFACT,EV,EVCAL,PI,W1,W2,BIGEXP
     ./CONSTN/ ZERO,ONE,TWO,THREE,FOUR,PT5,PT25
     ./INOPT1/ IN1(300)
     ./INOPT2/ IN2(300)
     ./INOPT3/ XN3(50)
     ./INOPT4/ XN4(50)
     ./MOPAC / IMOPAC
     ./NBFILE/ NBF(20)
      LOGICAL PRT
C *** FILE NUMBERS.
      NB5    = NBF(5)
      NB6    = NBF(6)
C *** INPUT VARIABLES.
      INP25  = IN2(64)
C *** PRINTING FLAG.
      PRT = .FALSE.
C *** READ INPUT AND INITIALIZE ARRAYS.
      IF(IMOPAC.EQ.0) THEN
         IF(INP25.GT.0) THEN
            READ(NB5,500) (IN1(I),I=211,226)
         ELSE
            IN1(211:226) = 0
         ENDIF
         IF(INP25.GT.1) THEN
            READ(NB5,510) (XN3(I),I=32,35)
         ELSE
            XN3(32:35) = ZERO
         ENDIF
      ELSE
C        CHECK IF ANY INP25 OPTION HAS BEEN CHANGED BY KEYWORD INPUT.
         DO I=211,226
            PRT = PRT .OR. (IN1(I).NE.0)
         END DO
         DO I=32,35
            PRT = PRT .OR. (XN3(I).NE.ZERO)
         END DO
      ENDIF
      IN2(211:226) = IN1(211:226)
      XN4(32:35)   = XN3(32:35)
C *** IMPOSE DEFAULT VALUES.
C     IN2(211)=IMODE, IN2(212)=INOUT.
      IF(IN2(2).GT.0) IN2(211)=1
      IF(IN2(212).LT.0) IN2(212)=0
C     FERMI TEMPERATURE IN K, DEFAULT VALUE 20000.
      IF(IN2(215).LE.0) IN2(215)=20000
C     IN2(223)=ICUTML, IN2(224)=MAXFMT, IN2(225)=LIMFMT.
      IF(IN2(223).LE.0) IN2(223)=42
      IF(IN2(224).EQ.0) IN2(224)=400
      IF(IN2(225).LE.0) IN2(225)=20
C     IN2(226)=IHCORR: DEFAULT -1.
      IF(IN2(226).NE.1) IN2(226)=-1
C     XN4(32)=DOMEGA, DEFAULT VALUE OF 0.2 AU.
      IF(XN4(32).LE.ZERO) XN4(32)=0.2D0*EV
C *** PRINTING SECTION.
      IF(JPRINT.GT.-5) THEN
         IF(INP25.GT.0 .OR. JPRINT.GE.5 .OR. PRT) THEN
            WRITE(NB6,100)
            WRITE(NB6,110) (IN2(I),I=211,226)
C           WRITE(NB6,120) (XN4(I),I=32,35)
C           IN2(211)=IMODE, IN2(212)=INOUT.
            IF(IN2(2).LE.0 .AND. IN2(211).LT.0) WRITE(NB6,310)
            IF(IN2(2).LE.0 .AND. IN2(211).EQ.1) WRITE(NB6,320)
            IF(IN2(2).LE.0 .AND. IN2(211).GT.1) WRITE(NB6,330)
            IF(IN2(212).EQ.1) WRITE(NB6,340)
            IF(IN2(212).GT.1) WRITE(NB6,350)
C           IN2(213)=IVBSE.
            IF(IN2(213).NE.0) WRITE(NB6,360)
            IF(IN2(213).LT.0) WRITE(NB6,370)
         ENDIF
      ENDIF
      RETURN
  100 FORMAT(///1X,'*** OPTIONS FOR INTEGRALS AND SCF TREATMENT ***',
     1       /  1X,'*** DEFINED EITHER EXPLICITLY OR BY DEFAULT ***')
  110 FORMAT(/  1X,'IMODE  =',I5,5X,'INOUT  =',I5,5X,'IVBSE  =',I5,
     1          5X,'IVBOVR =',I5,5X,'IFERMI =',I5,
     2       /  1X,'NFLOAT =',I5,5X,'NDOCC  =',I5,5X,'IOP218 =',I5,
     3          5X,'IOP219 =',I5,5X,'IOP220 =',I5,
     4       /  1X,'ICUTZS =',I5,5X,'ITOLFM =',I5,5X,'ICUTML =',I5,
     5          5X,'MAXFMT =',I5,5X,'LIMFMT =',I5,
     4       /  1X,'IHCORR =',I5)
C    6       /  1X,'MLROOT =',I5,5X,'MCGPRE =',I5,5X,'MCGUPD =',I5,
C    7          5X,'MPSCAL =',I5,5X,'MCUTAU =',I5,
C    8       /  1X,'MCUTM  =',I5,5X,'MCUTF  =',I5,5X,'MCUTP  =',I5,
C    9          5X,'MCUT1  =',I5,5X,'MCUT2  =',I5,
C    A       /  1X,'MCUTR  =',I5)
  120 FORMAT(// 1X,'DOMEGA =',F10.5,' EV    ORB. GAUSSIAN BROADENING')
C 120 FORMAT(// 1X,'FCUTF  =',G10.3,' EV    CUTOFF FOR FOCK MATRIX',
C    1       /  1X,'FCUTP  =',G10.3,'       CUTOFF FOR DENSITY MATRIX',
C    2       /  1X,'FCUT1  =',G10.3,' EV    CUTOFF FOR 1-EL INTEGRALS',
C    3       /  1X,'FCUT2  =',G10.3,' EV    CUTOFF FOR 2-EL INTEGRALS',
C    4       /  1X,'FCUTR  =',G10.3,' A     CUTOFF FOR DISTANCES'/)
  310 FORMAT(/  1X,'TWO-ELECTRON INTEGRALS HELD IN MEMORY AS A MATRIX')
  320 FORMAT(/  1X,'TWO-ELECTRON TWO-CENTER INTEGRALS HELD IN MEMORY ',
     1             'IN SEQUENTIAL ORDER')
  330 FORMAT(/  1X,'TWO-ELECTRON INTEGRALS SAVED ON FILE IF THEY ',
     1             'CANNOT BE HELD IN MEMORY')
  340 FORMAT(/  1X,'DENSITY MATRIX IS REGULARLY SAVED ON FILE ',
     1             'TO REDUCE THE MEMORY REQUIREMENTS')
  350 FORMAT(/  1X,'INTERMEDIATE AND FINAL SCF RESULTS ARE SAVED ',
     1             'ON FILES TO REDUCE MEMORY REQUIREMENTS')
  360 FORMAT(/  1X,'INTEGRALS AND OTHER INPUT DATA ARE SAVED ON ',
     1             'FILE FOR USE IN A SEPARATE VALENCE BOND PROGRAM')
  370 FORMAT(/  1X,'STOP AFTER SAVING THESE DATA')
  500 FORMAT(16I5)
  510 FORMAT(8G10.5)
      END
